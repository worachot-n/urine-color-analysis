def _circle_polygon_overlap(cx, cy, radius, polygon_coords):
    """
    Approximate the overlap area (pixels) between a circle and a polygon
    using rasterized binary masks.

    Args:
        cx, cy:          Circle center (int)
        radius:          Circle radius (int)
        polygon_coords:  (N, 2) float32 array of polygon vertices

    Returns:
        Overlap area in pixels (float).
    """
    pts   = polygon_coords.astype(np.int32)
    x_min = max(0, int(np.min(pts[:, 0])) - 1)
    y_min = max(0, int(np.min(pts[:, 1])) - 1)
    x_max = int(np.max(pts[:, 0])) + 1
    y_max = int(np.max(pts[:, 1])) + 1

    w = x_max - x_min + 1
    h = y_max - y_min + 1
    if w <= 0 or h <= 0:
        return 0.0

    poly_mask = np.zeros((h, w), dtype=np.uint8)
    shifted   = pts - np.array([x_min, y_min], dtype=np.int32)
    cv2.fillPoly(poly_mask, [shifted.reshape(-1, 1, 2)], 255)

    circle_mask = np.zeros((h, w), dtype=np.uint8)
    cv2.circle(circle_mask, (cx - x_min, cy - y_min), radius, 255, -1)

    intersection = cv2.bitwise_and(poly_mask, circle_mask)
    return float(cv2.countNonZero(intersection))


def _canonical_slot_id(r: int, c: int):
    """
    Return the slot_id for grid cell (row_span r, col_span c), or None.

    Row spans:  0 = reference row (skip), 1-12 = sample rows
    Col spans:  0 = ZZ dead zone (skip), 1-15 = data columns
    """
    if c == 0 or r == 0 or r > 12 or c > 15:
        return None
    group_num = math.ceil(r / 3)          # 1-4
    level_idx = (c - 1) // 3             # 0-4
    wgr       = (r - 1) % 3             # within-group row  0-2
    wbc       = (c - 1) % 3             # within-block col  0-2
    slot_num  = wgr * 3 + wbc + 1       # 1-9
    return f"A{group_num}{slot_num}_{level_idx}"


def _slot_id_to_rc(slot_id: str):
    """
    Reverse of _canonical_slot_id — returns (row_span, col_span).
    Used for tie-breaking centroid distance in warped space.
    """
    g  = int(slot_id[1])                         # group 1-4
    s  = int(slot_id[2])                         # slot  1-9
    l  = int(slot_id.split("_")[1])              # level 0-4
    wgr = (s - 1) // 3                           # within-group row
    wbc = (s - 1) % 3                            # within-block col
    r   = (g - 1) * 3 + wgr + 1                 # row span 1-12
    c   = l * 3 + wbc + 1                        # col span 1-15
    return r, c


def _bbox_polygon_overlap(x1: int, y1: int, x2: int, y2: int,
                           polygon_coords: np.ndarray) -> int:
    """
    Pixel-level intersection area between a bounding-box rectangle and a
    polygon, using rasterized binary masks (same technique as
    _circle_polygon_overlap).

    Returns overlap pixel count (0 if no intersection).
    """
    pts    = polygon_coords.astype(np.int32)
    px_min = max(0, int(np.min(pts[:, 0])) - 1)
    py_min = max(0, int(np.min(pts[:, 1])) - 1)
    px_max = int(np.max(pts[:, 0])) + 1
    py_max = int(np.max(pts[:, 1])) + 1

    w = px_max - px_min + 1
    h = py_max - py_min + 1
    if w <= 0 or h <= 0:
        return 0

    # Fast AABB reject — if bbox and polygon bounding rect don't overlap, skip
    if x2 <= px_min or x1 >= px_max or y2 <= py_min or y1 >= py_max:
        return 0

    poly_mask = np.zeros((h, w), dtype=np.uint8)
    shifted   = pts - np.array([px_min, py_min], dtype=np.int32)
    cv2.fillPoly(poly_mask, [shifted.reshape(-1, 1, 2)], 255)

    bbox_mask = np.zeros((h, w), dtype=np.uint8)
    bx1 = max(0, x1 - px_min)
    by1 = max(0, y1 - py_min)
    bx2 = min(w - 1, x2 - px_min)
    by2 = min(h - 1, y2 - py_min)
    if bx2 > bx1 and by2 > by1:
        cv2.rectangle(bbox_mask, (bx1, by1), (bx2, by2), 255, -1)

    return int(cv2.countNonZero(cv2.bitwise_and(poly_mask, bbox_mask)))


